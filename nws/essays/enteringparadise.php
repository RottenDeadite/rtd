    <p>Those of you who were around for the #memospore ARG(s) may remember a <a href="../css/images/SSbCZ5W.png" class="imgModal">certain clue</a> given to us in a Youtube video.</p>
	<p>The four figures on each corner appear to be the Four Corners of the House of Troubles. The figure in the center might seem to be hard to identify unless you happen to have an <a href="../css/images/Ol0ODbJ.jpg" class="imgModal">old piece of Morrowind concept art</a> on your hands.</p>
