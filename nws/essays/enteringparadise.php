	<p>So what can we make of the triangular symbol in Boethiah's hands? The easiest link to make is that it symbolizes ALMSIVI, the hybrid god-entity of whom Boethiah is the "mother." A more liberal dose of inference might suggest that it symbolizes the triangular gate, as described in Sermon 21. Naturally, that's the one I'm going with. Why? Well, there's more than just simple geometry at play here. There's magical geometry!</p>
	<h3>The Universe</h3>
	<p>The thing about the Tarot and the Elder Scrolls universe is that there seems to be some solid connections, but not many. This seems to be one of the better ones.</p>
	<p><a href="zawarudo" class="htmlModal">The World</a> card is called "The Universe" by Aleister Crowley and that's how I'm going to refer to it here, because the specifics of this card's symbolism vary dramatically from one system to another. It's numbered 21, the last of the Major Arcana, and no matter the system it often uses the following symbols:</p>
	<ul>
		<li>A central figure, often a naked woman, is shown dancing or hovering above the Earth, holding a staff or other object in each hand.</li>
		<li>She is surrounded by a circular or oval border that is sometimes an ouroboros.</li>
		<li>The border is, in turn, surrounded by four figures in each corner of the image. Sometimes these are Cherubs, sometimes the four Evangelists, sometimes four symbols to represent the four corners of the Earth or the four fixed elements in alchemy.</li>
	</ul>
	<p>These four figures surrounding a central Mother-figure reminded me immediately of the first screenshot, of Boethiah and the Four Corners of the House of Troubles. Almalexia, often called "Mother," is the anticipation of Boethiah. The Four Corners could be considered her "angels," if you squint a little. But the fact that Boethiah is cradling the Triangular Gate like a child, is particularly revealing, and I'll mention why in a moment.</p>
	<p>Because the Universe is the final card in the Major Arcana, it is considered the ultimate "goal" of the Fool's Journey, a "story" of the spiritual and metaphysical enlightenment of the Fool (the first card in the Major Arcana). This is relevant to Elder Scrolls mysticism because of the way the Universe is described by Crowley and other mystics (get ready for your hair to stand on end):</p>
	<blockquote>
		<p>The drop disappears into the ocean, and the ocean pours into the drop. This completion is, at the same time, a new beginning on a higher level of being. The final goal is reached - the return to the original cosmic Oneness.</p>
		<p>Now you see yourself and the world as it really is. [...] You are whirling, caught up in the perpetual dancing motion of the universe. The boundaries of your small >I< dissolve in orgasmic union with the universe.</p>
	</blockquote>
	<p>The Universe card in the Tarot is a pretty clear depiction of attaining the state of CHIM.</p>
	<p>Boethiah holds the Triangular Gate at the heart of the Second Serpent.</p>
	<blockquote>
		<p>'Look at the secret triangular gate sideways and you see the secret Tower.</p>
		<p>The secret Tower within the Tower is the shape of the only name of God, I.'</p>
	</blockquote>
	<p>What other Daedra is more concerned with the ULTIMATE Plot? The constant cycle of the Rebel & King? The Prince of Plots. Furthermore we are told "…the universal plot […] is begetting," which makes it easy to make a connection to the concept of the enantiomorphic event as the act of begetting: as the Father and Mother beget the Son, so to do Anu and Padomay beget the Amaranth.</p>
	<p>Boethiah holds the Triangular Gate. Boethiah, our Mother, holds the secret to CHIM.</p>
	<p>In the memospore clue I linked above, Boethiah is the center of the Universe card, bordered on all sides by the guardians of the Four Corners of the House of Troubles. Whether they are complicit or not, they help protect the Secret Triangular Gate.</p>
	<blockquote>
		<p>He that enters Paradise enters his own Mother.</p>
	</blockquote>
	<p>I could go on quoting lines from the 36 Lessons and the Commentaries that back this concept up, but there's just so many... Seriously, go read them and you'll see what I mean. I have to say, of all the metaphysical connections I've made in TES over the years, this one really holds up well under scrutiny.</p>
