	<p class="heading" id="foreword">The Fire of Vivec</p>
	<p>Vivec's Fire is mentioned more than a few times in the 36 Lessons. The most memorable being the blazing halo of fire around his head, and the line in Vivec's prayer, "The fire is mine, let it consume thee."</p>

	<p>When the term isn't used to describe literal fire, it gets a few other more obtuse mentions, in Sermon 6:</p>

	<blockquote><p>Above me is a challenge, which bathes itself in fire and is the essence of a god</p></blockquote>

	<p>In Sermon 10:</p>

	<blockquote><p>A short season of towers, a rundown absolution, and what is this, what is this but fire under your eyelid?</p></blockquote>

	<p>In Sermon 20:</p>

	<blockquote><p>This was the birth of the first Whirling School. Before, there had only been the surface thought of fire.</p></blockquote>

	<p>And in Sermon 31:</p>

	<blockquote><p>Afterwards, I dared to take on the sacred fire and realized there was no equilibrium within the ET'ADA.</p></blockquote>

	<p>It's pretty obvious that Vivec isn't talking about literal fire, but the fire metaphor clearly represents a common and repeated concept. But before I begin to explain this concept, let's go over some terminology, just to make sure everyone's on board.</p>

	<h3>Love</h3>

	<p>When I speak of Love, I'm talking about the joining of two opposites. In the 36 Lessons, this is often the term used to label the act of becoming divine, of growing to know your own inner divinity, which is the same divinity all beings share. This Love can take many forms: as small as meditation, as basic as washing the dishes, as enormous as marriage, as violent as war.</p>

	<p>Love is only real when it is done according to one's own Will, which is something like destiny, and unique to every individual. You guys remember that other essay I wrote? Okay, good.</p>

	<h3>Desire and Reaction</h3>

	<p>Performing Love under the direction of your Will can only be done without desire for result. Imagine a boat floating down a river. Steering against the current causes disruptions and ripples. Steering with the current is Love under Will. Don't perform actions out of the desire to see their result, but rather for the sake of the actions themselves, like a street musician playing music purely for the experience itself. Not out of desire for the tips he receives or the adoration of the crowd. When work is done Under Will, the work is accomplished without desire for a reward.</p>

	<p>The Bhagavad Gita sometimes talks about that desire as a "reaction." If you drink a sweet-tasting beverage, you experience a pleasurable reaction. If you lose a game of poker, your reaction is one of disappointment. If a loved one dies, you experience a reaction of sadness and loss. Pleasure and pain of the senses, emotion, and the conscious mind are all reactions, good and bad, that must be eliminated if one is to achieve enlightenment.</p>

	<p>Try to think of reaction and desire as the same concept, but approached from different directions.</p>

	<h3>Perfect Knowledge</h3>

	<p>Perfect Knowledge is the term used when you understand the full expanse of your own divine Will. According to some commentaries on the Bhagavad Gita:</p>

	<blockquote><p>Perfect knowledge of self and Superself and of their relationship is compared herein to fire. This fire not only burns up all reactions to impious activities, but also all reactions to pious activities, turning them to ashes. [â€¦] When one is in complete knowledge, all reactions, both a priori and a posteriori, are consumed. In the Vedas it is stated, ubhe uhaivaisa ete taraty amrtah sadhv-asadhuni: "One overcomes both the pious and impious interactions of work."</p></blockquote>

	<p>So from that we can reach the conclusion that Perfect Knowledge arises from the skill of doing Love under Will. <strong>Perfect Knowledge destroys reactions</strong>. Without reaction, there can be no desire. Only once you've given up everything can you be free to do anything.</p>

	<p>Now let's put it all together.</p>

	<h3>The Fire is His. Also, Yours.</h3>

	<p>As with other concepts in the 36 Lessons, Vivec's Fire is a reference to a real-life religious concept known by the same name in the Bhagavad Gita. In the Bhagavad Gita, "Fire" is a metaphor for Perfect (or transcendental) Knowledge, which is to say it represents the wisdom gained from fully comprehending the true structure of the universe.</p>

	<p>Reminds you of CHIM, right? Well, it's a little like that.</p>

	<p>This is maybe a little more complicated than the TES equivalent of comprehending the Wheel and the Tower and the Godhead. True transcendental knowledge is more akin to CHIM in that your understanding of reality and your relationship to it must permeate your entire being, your mind and your soul. In doing so, I think it creates something like Love Under Will.</p>

	<p>Therefore the Fire destroys all reactions to every kind of stimuli, both intuitive and justified. A possessor of such power performs "work" (productive actions) without desire of any kind, not even with the desire for the work to be completed.</p>

	<p>For those of you still struggling with this concept of action without desire, it may help to realize that many of you have already spent some time in a version of this state.</p>

	<p>Recall the image of someone resting by the fire, knitting a scarf, smiling to themselves, lost in the moment of the task at hand.</p>
	<p>Have you ever found yourself enjoying a video game, floating in a state of supreme concentration, yet somehow it doesn't seem to require that much effort?</p>
	<p>Ever start a homework assignment for school, and then suddenly realize that you've finished the entire thing in one go? Remember how it felt like almost no time had passed? It seemed easy, didn't it?</p>
	<p>Psychologists call this a "Flow State," and many programmers and nerds in general speak of it as the perfect point where productivity and concentration combine to form a state of almost euphoric output.</p>

	<h3>Summary</h3>

	<p>From this we can summarize the Hindu concept of Fire as supreme knowledge, skill, and ability. It removes (or destroys) all reaction, both negative and positive, bringing about a peace of mind known as Nirvana.</p>

	<p>In the Elder Scrolls, it is the God-like Knowledge that enables Vivec to perform Love under Will, the fulfillment of the Divine Will without desire for outcome. The halo, Vivec's fiery halo, is the representation of the fiery knowledge that enables, accompanies, and in fact is Vivec's spiritual divinity.</p>

	<p><strong>TL;DR</strong>: Vivec's Fire is a halo-styled representation or metaphor for his realization of I AM ALL ARE WE and his capacity to perform Love under Will. It is a key part of his Godlike power as well as one of its many visual representations.</p>

	<p>And once again we come to the common metaphysical theme of the Elder Scrolls. Enantiomorphs, CHIM, ALMSIVI, Talos, Mundus... These are all examples, iterations, and products of Love.</p>

	<p class="horizline"></p>

	<p>So just for giggles, let's go back over some of the mentions of Fire in the Lessons and think about what concept is being suggested:</p>

	<blockquote><p>Above me is a challenge, which bathes itself in fire and is the essence of a god</p></blockquote>

	<p>The "challenge" here is Amaranth, the state which Vivec attempted to reach, but could not. It is bathed in Fire because it is a state of complete sensual separation, pure Will.</p>

	<blockquote><p>A short season of towers, a rundown absolution, and what is this, what is this but fire under your eyelid?</p></blockquote>

	<p>This possibly describes Vivec's experience attaining CHIM, after a brief relationship with the First and Hidden towers, and then a weak forgiveness of his sins (his betrayal of Nerevar, most likely) by himself, the now-God.</p>

	<blockquote><p>This was the birth of the first Whirling School. Before, there had only been the surface thought of fire.</p></blockquote>

	<p>Before the Whirling School, we only had a conceptualization of Fire, just the slightest inclination. The Whirling School was born to explore this concept.</p>

	<blockquote><p>Afterwards, I dared to take on the sacred fire and realized there was no equilibrium within the ET'ADA.</p></blockquote>

	<p>After Vivec attained CHIM, he realized the Et'Ada were as chaotic as Mundus. As above, so below.</p>

	<p>And what does Vivec mean in his prayer? "The fire is mine, let it consume thee." Perhaps he speaks of the way Fire burns away all desire. He asks that the recipient of the prayer let the fire of perfect knowledge consume, absorb, purify and change into pure Love, and find rest in the arms of Mother Boethiah.</p>

	<p class="horizline"></p>

	<p>Here's another interesting thing I found while researching this:</p>

	<p>Visually this Fire is depicted by the 36 Lessons as a kind of fire that envelopes Vivec's head. It's perhaps easy to see a reference to the spiritual concept of the Halo, which has been used in one form or another by nearly every major religion in history to depict the light of divinity, among other things.</p>

	<p>Interestingly, some Christian aspects will use the Halo to depict the concept of the "Uncreated Light," a divine radiance that can be used in depictions of purified saints as well as heavenly entities. The Uncreated Light was first suggested by a monk arguing for Hesychasm, a more meditative and insular method of prayer that shares some of the principles of Hindu meditation, including the reduction of the senses and the reactions they cause. That reduction is actually vital to the entire concept of this spiritual fire; you burn away all reactions.</p>

	<p>So in a way, both religions, Christianity and Hinduism, arrived at the same visual representation for the same rather specific transcendental phenomenon achieved via similar meditative techniques. But it seems as though they did this completely independent of each other. Isn't that fascinating?</p>
